import { SeverityLevel } from "@/domain/entities/risk-evaluator";

export interface PredictionFactor {
  incidentType: string;
  isExtremeWeather: boolean;
  victimCount: number;
  hasHospitalsNearby: boolean;
  isNightTime: boolean;
  geminiPredictions: {
    threatType: string;
    probability: number;
    impact: SeverityLevel;
    estimatedTimeframe: string;
    confidence: number;
  }[];
}

export interface GeneratedPrediction {
  threatType: string;
  probability: number; // 0.0 - 1.0
  impact: SeverityLevel;
  estimatedTimeframe: string;
  confidence: number; // 0.0 - 1.0
}

export class PredictionEngine {
  
  /**
   * Generates localized threat predictions by combining system heuristic calculations
   * with predictions generated by Gemini.
   */
  public generatePredictions(input: PredictionFactor): GeneratedPrediction[] {
    const predictionsMap: Map<string, GeneratedPrediction> = new Map();

    // 1. Initialize baseline predictions based on incident type
    this.addBaselines(input, predictionsMap);

    // 2. Overwrite/merge with Gemini's detailed predictions
    for (const geminiPred of input.geminiPredictions) {
      const typeKey = geminiPred.threatType.toLowerCase();
      
      const baseline = predictionsMap.get(typeKey);
      if (baseline) {
        // Blend probabilities: 50% system state heuristic, 50% Gemini assessment
        const blendedProbability = Math.min(1.0, Math.max(0.0, baseline.probability * 0.5 + geminiPred.probability * 0.5));
        const blendedConfidence = Math.min(1.0, Math.max(0.0, baseline.confidence * 0.4 + geminiPred.confidence * 0.6));
        
        predictionsMap.set(typeKey, {
          threatType: geminiPred.threatType,
          probability: parseFloat(blendedProbability.toFixed(2)),
          impact: geminiPred.impact, // Prefer Gemini's impact assessment
          estimatedTimeframe: geminiPred.estimatedTimeframe,
          confidence: parseFloat(blendedConfidence.toFixed(2)),
        });
      } else {
        predictionsMap.set(typeKey, {
          threatType: geminiPred.threatType,
          probability: geminiPred.probability,
          impact: geminiPred.impact,
          estimatedTimeframe: geminiPred.estimatedTimeframe,
          confidence: geminiPred.confidence,
        });
      }
    }

    return Array.from(predictionsMap.values());
  }

  private addBaselines(input: PredictionFactor, map: Map<string, GeneratedPrediction>): void {
    const type = input.incidentType.toLowerCase();

    // Baseline: Casualty Increase
    let casualtyProb = 0.1;
    if (input.victimCount > 0) casualtyProb = 0.4;
    if (input.victimCount > 10) casualtyProb = 0.7;
    if (input.isNightTime) casualtyProb += 0.1;
    
    map.set("casualty_increase", {
      threatType: "casualty_increase",
      probability: Math.min(0.95, casualtyProb),
      impact: casualtyProb > 0.6 ? "HIGH" : "MEDIUM",
      estimatedTimeframe: "1h",
      confidence: 0.8,
    });

    // Baseline: Road Blockage
    let roadProb = 0.2;
    if (type.includes("accident") || type.includes("collapse")) roadProb = 0.8;
    if (type.includes("flood")) roadProb = 0.6;
    
    map.set("road_blockage", {
      threatType: "road_blockage",
      probability: roadProb,
      impact: roadProb > 0.7 ? "HIGH" : "MEDIUM",
      estimatedTimeframe: "30m",
      confidence: 0.85,
    });

    // Baseline: Flood Spread
    if (type.includes("flood")) {
      const floodProb = input.isExtremeWeather ? 0.85 : 0.5;
      map.set("flood_spread", {
        threatType: "flood_spread",
        probability: floodProb,
        impact: input.isExtremeWeather ? "CRITICAL" : "HIGH",
        estimatedTimeframe: "4h",
        confidence: 0.75,
      });
    }

    // Baseline: Fire Spread
    if (type.includes("fire") || type.includes("explosion")) {
      const fireProb = input.isExtremeWeather ? 0.8 : 0.6;
      map.set("fire_spread", {
        threatType: "fire_spread",
        probability: fireProb,
        impact: fireProb > 0.7 ? "CRITICAL" : "HIGH",
        estimatedTimeframe: "15m",
        confidence: 0.8,
      });
    }

    // Baseline: Infrastructure Damage
    let infraProb = 0.1;
    if (type.includes("earthquake") || type.includes("explosion") || type.includes("collapse")) {
      infraProb = 0.9;
    } else if (type.includes("flood") || type.includes("leak")) {
      infraProb = 0.5;
    }
    map.set("infrastructure_damage", {
      threatType: "infrastructure_damage",
      probability: infraProb,
      impact: infraProb > 0.7 ? "CRITICAL" : "MEDIUM",
      estimatedTimeframe: "immediate",
      confidence: 0.8,
    });

    // Baseline: Hospital Overload
    if (input.hasHospitalsNearby && input.victimCount > 10) {
      map.set("hospital_overload", {
        threatType: "hospital_overload",
        probability: 0.75,
        impact: "HIGH",
        estimatedTimeframe: "2h",
        confidence: 0.7,
      });
    }
  }
}

export const predictionEngine = new PredictionEngine();
